package Entidades;

public class Imovel {

	private Integer qtdQuartos;
	private Integer qtdBanheiros;
	private Integer qtdCozinhas;
	private Integer qtdAndares;
	private Integer qtdGaragens;
	private Integer qtdAnimais;
	private Integer tamanhoEstimadoCasa;

	public Integer getQtdQuartos() {
		return qtdQuartos;
	}

	public void setQtdQuartos(Integer qtdQuartos) {
		this.qtdQuartos = qtdQuartos;
	}

	public Integer getQtdBanheiros() {
		return qtdBanheiros;
	}

	public void setQtdBanheiros(Integer qtdBanheiros) {
		this.qtdBanheiros = qtdBanheiros;
	}

	public Integer getQtdCozinhas() {
		return qtdCozinhas;
	}

	public void setQtdCozinhas(Integer qtdCozinhas) {
		this.qtdCozinhas = qtdCozinhas;
	}

	public Integer getQtdAndares() {
		return qtdAndares;
	}

	public void setQtdAndares(Integer qtdAndares) {
		this.qtdAndares = qtdAndares;
	}

	public Integer getQtdGaragens() {
		return qtdGaragens;
	}

	public void setQtdGaragens(Integer qtdGragens) {
		this.qtdGaragens = qtdGragens;
	}

	public Integer getQtdAnimais() {
		return qtdAnimais;
	}

	public void setQtdAnimais(Integer qtdAnimais) {
		this.qtdAnimais = qtdAnimais;
	}

	public Integer getTamanhoEstimadoCasa() {
		return tamanhoEstimadoCasa;
	}

	public void setTamanhoEstimadoCasa(Integer tamanhoEstimadoCasa) {
		this.tamanhoEstimadoCasa = tamanhoEstimadoCasa;
	}

}